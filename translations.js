// Language switcher functionality
let currentLang = localStorage.getItem('preferred-language') || 'en';

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('preferred-language', lang);
    
    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });
    
    // Update placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        if (translations[lang][key]) {
            element.placeholder = translations[lang][key];
        }
    });
    
    // Update active language button if exists
    const langEn = document.getElementById('lang-en');
    const langNl = document.getElementById('lang-nl');
    
    if (langEn && langNl) {
        langEn.classList.toggle('bg-indigo-600', lang === 'en');
        langEn.classList.toggle('text-white', lang === 'en');
        langEn.classList.toggle('hover:bg-gray-100', lang !== 'en');
        
        langNl.classList.toggle('bg-indigo-600', lang === 'nl');
        langNl.classList.toggle('text-white', lang === 'nl');
        langNl.classList.toggle('hover:bg-gray-100', lang !== 'nl');
    }
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
    setLanguage(currentLang);
    
    // Add click handlers for language buttons if they exist
    const langEn = document.getElementById('lang-en');
    const langNl = document.getElementById('lang-nl');
    
    if (langEn) langEn.addEventListener('click', () => setLanguage('en'));
    if (langNl) langNl.addEventListener('click', () => setLanguage('nl'));
});

// Export for use in other scripts
if (typeof module !== 'undefined' && module.exports) {
    module.exports = { translations, setLanguage, currentLang };
}
